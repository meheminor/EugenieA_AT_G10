package hm1.prebase;

public class NumberPrinting {
    public static void main(String[] args) {

        NumberPrinting numberPrint = new NumberPrinting();
        numberPrint.numberPrint();

    }

    public void numberPrint() {
        for (int i = 1; i < 11;  i++) {
            System.out.println(i);
        }
    }

}
