package hm1.prebase;

public class MathOperatorsExample {
    public static void main(String[] args) {
        MathOperatorsExample math = new MathOperatorsExample();
        math.math(120,3);
    }

    public void math(int ab,int bb){
        System.out.println(ab+bb);
        System.out.println(ab-bb);
        System.out.println(ab*bb);
        System.out.println(ab/bb);
    }
}
