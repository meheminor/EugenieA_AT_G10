package src.hm1.base;

public class TrainMethodsPrimitive {
    public void printInt(int i){
        System.out.println("I got the number: "+ i);
    }
    public void printLong(long l){
        System.out.println("I got the long number: "+ l);
    }
    public void printChar(char c){
        System.out.println("I got the symbol: "+ c);
    }
    public void printFloat(float f){
        System.out.println("I got the float number: "+ f);
    }
    public void printDouble(double d){
        System.out.println("I got the double number: "+ d);
    }
    public void printShort(short s){
        System.out.println("I got the short number: "+ s);
    }
    public void printByte(byte b){
        System.out.println("I got the very short number: "+ b);
    }
    public void printBoolean(boolean bool){
        System.out.println("I got this boolean: "+ bool);
    }

}
